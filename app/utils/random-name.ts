function pick<T>(arr: T[]): T {
	return arr[Math.floor(Math.random() * arr.length)];
}

export function generateTwoWordSlug(separator = "-"): string {
	const a = pick(ADJECTIVES);
	const n = pick(NOUNS);
	return `${a}${separator}${n}`.toLowerCase();
}
